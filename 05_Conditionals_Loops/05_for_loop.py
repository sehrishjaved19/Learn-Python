# A FOR LOOP is used to iterate through a sequence like list, tuple, or string[iterables]

# The range() function in pyhton is used to generatw a sequence of number
#range(start, stop, step_size)

for i in range(0, 4):
    print(i)
#Another way
for i in range(4):
    print(i)



#for loop for list
lst=[1,4,6,234,6,764]
for i in lst:
    print(i)
#i is a loop variable that refers to each item in the list, one by one.




#for loop for tuple
tup=(1,4,5,876,45)
for items in tup:
    print(items)





